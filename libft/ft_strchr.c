#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s && s[i] && s[i] != c)
	{
		i += 1;
	}
	if (s && s[i] == c)
		return ((char*)s + i);
	return (NULL);
}
